# Write a program that will input a
# number in feet, convert, and display
# the number in yard

print()
print("\t==== FEET TO YARD CONVERTER ====")
print()

feet = float(input("\tEnter the number of feet : "))

yard = feet / 3

print()
print("\t{0:.2f} feet/foot >>>>>>>> {1:.2f} yard(s)".format(feet, yard))
print()
print("\t==== END OF PROGRAM ====")
print()
