# Exercise No. 4
# Write a program that will compute
# the sum and product of two input numbers.

print()
print("\t==== SUM AND PRODUCT ====")
print()

num_1 = int(input("\tEnter the first number : "))
num_2 = int(input("\tEnter the second number : "))

summation = num_1 + num_2
product = num_1 * num_2

print()
print("\tSum of {0} and {1} is {2}".format(num_1, num_2, summation))
print("\tProduct of {0} and {1} is {2}".format(num_1, num_2, product))
print()
print("\t==== END OF PROGRAM ====")
print()
